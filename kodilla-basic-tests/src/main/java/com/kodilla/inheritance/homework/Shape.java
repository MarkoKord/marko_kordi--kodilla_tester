package com.kodilla.inheritance.homework;

import java.awt.geom.Area;

public abstract class Shape {
    private int area;
    private int circuit;

    public Shape() {
        this.area=area;
        this.circuit=circuit;
    }

    public int getArea() {
        return area;
    }

    public int getCircuit() {
        return circuit;
    }
}



